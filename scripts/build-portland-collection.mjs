// Generates the standalone Portland Collection index page:
//   public/portland-collection/index.html   — full static tune list (SEO: every
//                                             tune name is in the HTML)
//   public/portland-collection/tunes-abc.json — id → ABC notation, fetched
//                                               lazily on first "Sheet music"
//
// Source data: scripts/portland-collection/tunes.json (built from the
// theportlandcollection.com tune lists matched against thesession.org).
// Run manually after updating the data: node scripts/build-portland-collection.mjs
//
// The page is deliberately NOT linked from the app — it's a standalone SEO /
// traffic page that links out to tunebox.net. Behaviour lives in app.js
// (hand-written, same directory); abcjs is lazy-loaded from /abcjs/ (copied
// out of node_modules by copy-vendor-assets.mjs on postinstall).
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const outDir = join(root, 'public', 'portland-collection');
mkdirSync(outDir, { recursive: true });

const tunes = JSON.parse(readFileSync(join(root, 'scripts', 'portland-collection', 'tunes.json'), 'utf8'));

const esc = (s) => s.replace(/[&<>"]/g, (c) => ({ '&': '&amp;', '<': '&lt;', '>': '&gt;', '"': '&quot;' }[c]));
const cap = (s) => s.charAt(0).toUpperCase() + s.slice(1);

// Stable ids follow source-data order; cards are emitted alphabetically.
const withIds = tunes.map((t, i) => ({ ...t, _id: 't' + i }));
const sorted = [...withIds].sort((a, b) => a.name.localeCompare(b.name));

const total = tunes.length;
const linked = tunes.filter((t) => t.url).length;
const playable = tunes.filter((t) => t.abc).length;
const types = [...new Set(tunes.map((t) => t.type).filter(Boolean))].sort();

// ---- ABC payload (lazy-fetched) ----
const abcMap = {};
for (const t of withIds) if (t.abc) abcMap[t._id] = t.abc;
writeFileSync(join(outDir, 'tunes-abc.json'), JSON.stringify(abcMap));

// ---- tune cards ----
// Row click (JS) expands the score; the side button links to thesession.org.
const cards = sorted.map((t) => {
  const chips = [
    `<span class="chip chip-book">Book ${t.book}</span>`,
    t.type ? `<span class="chip">${esc(cap(t.type))}</span>` : '',
    t.closest ? `<span class="chip chip-approx" title="Matched to thesession.org by name similarity — may differ from the book&#39;s version">&asymp; match</span>` : '',
    !t.url ? `<span class="chip chip-na">Not on thesession</span>` : '',
  ].filter(Boolean).join('');
  const caret = t.abc ? `<span class="caret" aria-hidden="true">&#9662;</span>` : '';
  const sessionBtn = t.url
    ? `<a class="btn-session" href="${esc(t.url)}" target="_blank" rel="noopener">thesession &#8599;</a>`
    : '';
  return `<article class="tune" data-id="${t._id}" data-n="${esc(t.name.toLowerCase())}" data-b="${t.book}" data-t="${esc(t.type || '')}" data-a="${t.abc ? 1 : 0}" data-l="${t.url ? 1 : 0}"${t.settings > 1 ? ` data-settings="${t.settings}"` : ''}>
<div class="tune-head"><span class="tune-name">${esc(t.name)}</span>${chips}${caret}${sessionBtn}</div>
<div class="tune-detail" hidden></div>
</article>`;
}).join('\n');

const typeOptions = types.map((t) => `<option value="${esc(t)}">${esc(cap(t))}</option>`).join('');

